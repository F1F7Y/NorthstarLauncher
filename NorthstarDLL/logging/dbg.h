#pragma once

//-----------------------------------------------------------------------------
// Log context
enum class eLogLevel : int
{
	INFO = 0,
	WARN = 1,
	ERROR = 2
};

//-----------------------------------------------------------------------------
// Log context
enum class eLog : int
{
	//-----------------------------------------------------------------------------
	// Common
	//-----------------------------------------------------------------------------
	NONE = 0,
	NS = 1,

	//-----------------------------------------------------------------------------
	// Script
	//-----------------------------------------------------------------------------
	SCRIPT_SERVER = 2,
	SCRIPT_CLIENT = 3,
	SCRIPT_UI = 4,

	//-----------------------------------------------------------------------------
	// Native
	//-----------------------------------------------------------------------------
	SERVER = 5,
	CLIENT = 6,
	UI = 7,
	ENGINE = 8,
	RTECH = 9,
	FS = 10,
	MAT = 11,
	AUDIO = 12,
	VIDEO = 13
};

void DevMsg(eLog eContext, const char* fmt, ...);
void Warning(eLog eContext, const char* fmt, ...);
void Error(eLog eContext, int nCode, const char* fmt, ...);
